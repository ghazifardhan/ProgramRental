    private Boolean formFill(){
        Boolean stat;
        if(((JTextField)mTanggalSewa.getDateEditor().getUiComponent()).getText().length() == 0){
            JOptionPane.showMessageDialog(null, "Tanggal Harus diisi");
            stat = false;
        } else if(mTotalHariSewa.getText().length() == 0){
            JOptionPane.showMessageDialog(null, "Total hari sewa Harus diisi");
            stat = false;
        } else if(cbStatusSewa.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(null, "Status sewa Harus diisi");
            stat = false;
        } else if(mTotalHarga.getText().length() == 0){
            JOptionPane.showMessageDialog(null, "Total harga Harus diisi");
            stat = false;
        } else {
            stat = true;
        }
        return stat;
    }
    private void generateKendaraan(){
        itemsKendaraan.clear();
        ResultSet rs = Database.selectData("daftar_kendaraan");
        try{
            while(rs.next()){
               itemsKendaraan.add(new Kendaraan(rs.getInt("id"), rs.getString("merk"), rs.getString("jenis"), rs.getInt("harga_per_hari")));
               cbKendaraan.addItem(rs.getString("jenis"));
            }
        } catch(SQLException ex){
            ex.getMessage();
        }
    }

    private void generateCustomer(){
        itemsCustomer.clear();
        ResultSet rs = Database.selectData("daftar_customer");
        try{
            while(rs.next()){
               itemsCustomer.add(new Customer(rs.getInt("id"), rs.getString("nama"), rs.getString("alamat"), rs.getString("id_type"), rs.getString("no_id"), rs.getString("no_hp"), rs.getString("umur")));
               cbCustomer.addItem(rs.getString("nama"));
            }
        } catch(SQLException ex){
            ex.getMessage();
        }
    }
    
    private void generateData(Integer id){
        String search = "a.id = " + id;
        ResultSet rs = Database.generateRentalSearch(search);
        try{
            while(rs.next()){
               cbKendaraan.setSelectedItem(rs.getString("jenis"));
               cbCustomer.setSelectedItem(rs.getString("nama"));
               ((JTextField)mTanggalSewa.getDateEditor().getUiComponent()).setText(rs.getString("tanggal_sewa"));
               mTotalHariSewa.setText(rs.getInt("total_hari_sewa")+"");
               cbStatusSewa.setSelectedItem(rs.getString("status_sewa"));
               mTotalHarga.setText(rs.getString("total_harga"));
            }
        } catch(SQLException ex){
            ex.getMessage();
        }
    }
    
