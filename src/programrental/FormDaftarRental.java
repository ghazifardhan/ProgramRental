    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        Integer id_kendaraan = itemsKendaraan.get(cbKendaraan.getSelectedIndex()).getId();
        Integer id_customer = itemsCustomer.get(cbCustomer.getSelectedIndex()).getId();
        String tanggal_sewa = ((JTextField)mTanggalSewa.getDateEditor().getUiComponent()).getText();
        Integer total_hari_sewa = Integer.parseInt(mTotalHariSewa.getText());
        String status_sewa = cbStatusSewa.getSelectedItem().toString();
        Integer total_harga = Integer.parseInt(mTotalHarga.getText());
        
        
        if(formFill() != true){
            formFill();
        } else {
            if(isEdit){
                String set = "id_kendaraan = '" + id_kendaraan + "', id_customer = '" + id_customer + "', tanggal_sewa = '" + tanggal_sewa + "', total_hari_sewa = '" + total_hari_sewa + "', status_sewa = '" + status_sewa + "', total_harga = '" + total_harga + "'";
                Database.updateData(tableName, set, k_id);
                DaftarRental v = new DaftarRental(jdp);
                jdp.add(v);
                v.setVisible(true);
                this.dispose();
            } else {
                String attr = "(id_kendaraan, id_customer, tanggal_sewa, total_hari_sewa, status_sewa, total_harga)";
                String value = "('"+id_kendaraan+"','"+id_customer+"','"+tanggal_sewa+"','"+total_hari_sewa+"','"+status_sewa+"','"+total_harga+"')";
                Database.insertData(tableName, attr, value);
                DaftarRental v = new DaftarRental(jdp);
                jdp.add(v);
                v.setVisible(true);
                this.dispose();
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

